-- Helper: resolves the current Supabase Auth user's wt_users.id (bigint)
create or replace function public.wt_my_id()
returns bigint
language sql
stable
security invoker
set search_path = public
as $$
  select id from public.wt_users where auth_user_id = auth.uid() limit 1;
$$;

grant execute on function public.wt_my_id() to anon, authenticated;

-- Task writes must be checked against the signed-in WorkTracker user. The legacy
-- v2 client still reads tasks before login during bootstrap, so SELECT remains
-- permissive, but INSERT/UPDATE/DELETE require owner/editor/admin access.
create or replace function public.wt_is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.wt_users
    where auth_user_id = auth.uid()
      and role = 'admin'
  );
$$;

