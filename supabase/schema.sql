do $$ declare t text;
begin
  foreach t in array array[
    'wt_users','wt_settings','wt_departments','wt_projects','wt_tasks','wt_milestones',
    'wt_updates','wt_attachments','wt_activity_log','wt_notifications','wt_webhooks','wt_sessions'
  ] loop
    execute format('drop policy if exists wt_anon_all on public.%I', t);
    execute format(
      'create policy wt_anon_all on public.%I for all to anon, authenticated using (true) with check (true)',
      t
    );
  end loop;
end $$;

drop policy if exists wt_storage_read on storage.objects;
